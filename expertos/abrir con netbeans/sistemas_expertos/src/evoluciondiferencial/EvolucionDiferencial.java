    public void verPoblacion(){
        String aux = "[ ";
        for(int i=0;i<4;i++){
            
            if(i!=3){
                aux = aux + vecPoblacion[i]+ ", ";
            }else{
                aux = aux + vecPoblacion[i] + " ]";
            }            
        }
        jLabel8.setText(aux);
        jLabel9.setText("( "+vecFuncion[0]+", "+vecFuncion[1]+", "+vecFuncion[2]+", "+vecFuncion[3]+" )");
    }
    public void evaluarFuncion(){
        for(int i=0;i<4;i++){
            vecFuncion[i]=(vecPoblacion[i]*vecPoblacion[i])+(vecPoblacion[i]*vecPoblacion[i]*vecPoblacion[i]);
        }        
    }
    
