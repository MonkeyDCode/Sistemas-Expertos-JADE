    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        M = Double.parseDouble(jTextField3.getText());
        Cr = Double.parseDouble(jTextField4.getText());
        if(jRadioButton3.isSelected()){
            bandera = 1;
        }else if(jRadioButton4.isSelected()){
            bandera = 2;
        }
        for(int x=0;x<3;x++){
            if(x==0){
                vista = new VistaResultado(Integer.toString(x+1), vecPoblacion, M, vecFuncion, Cr, bandera);                
                vista.setVisible(true);
                if(bandera==1){
                    vista.setTitle("MAXIMIZA");
                }else{
                    vista.setTitle("MINIMIZA");
                }
                this.vecPoblacion=vista.getVectorPoblacion();
                this.vecFuncion=vista.getValorPoblacion();
            }
            if(x==1){
                vista = new VistaResultado(Integer.toString(x+1), vecPoblacion, M, vecFuncion, Cr, bandera);
                vista.setLocationRelativeTo(null);
                vista.setVisible(true);
                if(bandera==1){
                    vista.setTitle("MAXIMIZA");
                }else{
                    vista.setTitle("MINIMIZA");
                }
                this.vecPoblacion=vista.getVectorPoblacion();
                this.vecFuncion=vista.getValorPoblacion();                
            }
            if(x==2){
                vista = new VistaResultado(Integer.toString(x+1), vecPoblacion, M, vecFuncion, Cr, bandera);
                vista.setLocation(900, 0);                
                vista.setVisible(true);
                if(bandera==1){
                    vista.setTitle("MAXIMIZA");
                }else{
                    vista.setTitle("MINIMIZA");
                }
                this.vecPoblacion=vista.getVectorPoblacion();
                this.vecFuncion=vista.getValorPoblacion();                
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

