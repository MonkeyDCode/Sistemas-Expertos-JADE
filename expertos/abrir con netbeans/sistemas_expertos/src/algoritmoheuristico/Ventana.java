    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(this.jRadioButton1.isSelected()==false && this.jRadioButton2.isSelected()==false){
            JOptionPane.showMessageDialog(null, "Seleciones Maximizar o Minimizar");
        }else{
            if(bandera==1){
            this.matriz = grafos.getMatriz(1);
            this.conexion = grafos.getConexion(1,this.getOpcion());
            bandera++;
            }else if(bandera==2){
                this.matriz = grafos.getMatriz(2);
                this.conexion = grafos.getConexion(2,this.getOpcion());
                bandera++;
            }else if(bandera==3){
                this.matriz = grafos.getMatriz(3);
                this.conexion = grafos.getConexion(3,this.getOpcion());
                bandera=1;
            }
            jTextArea1.setText(null);
            verMatriz();
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

